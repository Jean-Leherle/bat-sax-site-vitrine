--Créer un bucket "batsax-media"

-- 1. Autoriser tout le monde à VOIR les images (Lecture publique)
CREATE POLICY "Images visibles par tous" 
ON storage.objects FOR SELECT 
USING ( bucket_id = 'batsax-media' );

-- 2. Autoriser les administrateurs (connectés) à AJOUTER des images (Upload)
CREATE POLICY "Upload pour les admins" 
ON storage.objects FOR INSERT 
WITH CHECK ( bucket_id = 'batsax-media' AND auth.role() = 'authenticated' );

-- 3. Autoriser les administrateurs à MODIFIER des images
CREATE POLICY "Modification pour les admins" 
ON storage.objects FOR UPDATE 
USING ( bucket_id = 'batsax-media' AND auth.role() = 'authenticated' );

-- 4. Autoriser les administrateurs à SUPPRIMER des images
CREATE POLICY "Suppression pour les admins" 
ON storage.objects FOR DELETE 
USING ( bucket_id = 'batsax-media' AND auth.role() = 'authenticated' );