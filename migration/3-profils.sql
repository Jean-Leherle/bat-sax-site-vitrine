-- 1. Création de la table des membres
CREATE TABLE public.members (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug TEXT UNIQUE NOT NULL, -- Identifiant pour l'URL (ex: 'pierre')
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  "photoUrl" TEXT NOT NULL,
  instruments JSONB NOT NULL DEFAULT '[]'::jsonb, -- Stockage flexible (JSON)
  "customFields" JSONB NOT NULL DEFAULT '[]'::jsonb, -- Stockage flexible (JSON)
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Sécurité RLS
ALTER TABLE public.members ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Accès public en lecture des membres" 
ON public.members FOR SELECT USING (true);

CREATE POLICY "Accès total membres pour les admins" 
ON public.members FOR ALL USING (auth.role() = 'authenticated');

