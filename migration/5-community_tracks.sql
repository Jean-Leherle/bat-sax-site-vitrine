DROP POLICY IF EXISTS "Lecture publique des profils" ON public.profiles;
CREATE POLICY "Lecture publique des profils" ON public.profiles FOR SELECT USING (true);

DROP POLICY IF EXISTS "Modification propre profil" ON public.profiles;
CREATE POLICY "Modification propre profil" ON public.profiles 
FOR UPDATE 
USING (auth.uid() = id) 
WITH CHECK (auth.uid() = id AND (role = 'user' OR public.is_admin())); 

DROP POLICY IF EXISTS "Admins full access profiles" ON public.profiles;
CREATE POLICY "Admins full access profiles" ON public.profiles FOR ALL USING (public.is_admin());

-- --- POLITIQUES COMMUNITY TRACKS ---
DROP POLICY IF EXISTS "Lecture publique suggestions" ON public.community_tracks;
