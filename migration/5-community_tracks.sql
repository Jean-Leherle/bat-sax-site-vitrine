DROP POLICY IF EXISTS "Auteur peut modifier titre et liens" ON public.community_tracks;
CREATE POLICY "Auteur peut modifier titre et liens" ON public.community_tracks 
FOR UPDATE 
USING (auth.uid() = user_id AND status = 'en_vote') 
WITH CHECK (auth.uid() = user_id AND status = 'en_vote');

DROP POLICY IF EXISTS "Suppr par auteur" ON public.community_tracks;
CREATE POLICY "Suppr par auteur" ON public.community_tracks 
FOR DELETE 
USING (auth.uid() = user_id AND status = 'en_vote');

DROP POLICY IF EXISTS "Admin full access tracks" ON public.community_tracks;
CREATE POLICY "Admin full access tracks" ON public.community_tracks FOR ALL USING (public.is_admin());

DROP POLICY IF EXISTS "Limite de 5 suggestions" ON public.community_tracks;
CREATE POLICY "Limite de 5 suggestions" ON public.community_tracks 
FOR INSERT 
WITH CHECK (
  auth.uid() = user_id 
  AND (SELECT count(*) FROM public.community_tracks WHERE user_id = auth.uid()) < 5
);

